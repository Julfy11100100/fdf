#include "libft.h"

void	ft_putnbr(int nb)
{
	long int	i;
	int			sign;

	i = nb;
	sign = 1;
	if (i == -2147483648)
	{
		write(1, "-2147483648", 11);
		return ;
	}
	if (i == 2147483647)
	{
		write(1, "2147483647", 10);
		return ;
	}
	if (i < 0)
	{
		i = i * -1;
		sign = -1;
	}
	if (sign == -1)
		ft_putchar('-');
	if (i / 10 != 0)
		ft_putnbr(i / 10);
	ft_putchar((i % 10) + 48);
}
