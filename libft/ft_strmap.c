#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	size_t	i;

	i = 0;
	if (s == NULL)
		return (0);
	str = (char *)malloc(sizeof(*f) * (ft_strlen(s) + 1));
	if (str == NULL)
		return (0);
	while (s[i] != '\0')
	{
		str[i] = f((char)s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
