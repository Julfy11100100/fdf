#include "libft.h"

int		ft_findchr(char *s, char c)
{
	if (s == NULL)
		return (0);
	if (c == '\0')
		return (1);
	while (*s)
	{
		if (*s == c)
			return (1);
		s++;
	}
	return (0);
}
